const mongoose = require("mongoose");

const tokenBlacklistSchema = new mongoose.Schema(
  {
    token: {
      type: String,
      required: true,
      unique: true,
    },
  },
  { timestamps: true },
);

tokenBlacklistSchema.index(
  { createdAt: 1 },
  { expiresAfterSeconds: 60 * 60 * 24 * 3 },
); // expire after 3 days

module.exports = mongoose.model("TokenBlacklist", tokenBlacklistSchema);
